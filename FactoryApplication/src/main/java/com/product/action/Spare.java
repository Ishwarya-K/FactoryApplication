package com.product.action;

public class Spare {
	private int spareId,price,quantityToProduceSingleProd,availableQuantity;
	private String spareName;
	public int getSpareId() {
		return spareId;
	}
	public void setSpareId(int spareId) {
		this.spareId = spareId;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getQuantityToProduceSingleProd() {
		return quantityToProduceSingleProd;
	}
	public void setQuantityToProduceSingleProd(int quantityToProduceSingleProd) {
		this.quantityToProduceSingleProd = quantityToProduceSingleProd;
	}
	public int getAvailableQuantity() {
		return availableQuantity;
	}
	public void setAvailableQuantity(int availableQuantity) {
		this.availableQuantity = availableQuantity;
	}
	public String getSpareName() {
		return spareName;
	}
	public void setSpareName(String spareName) {
		this.spareName = spareName;
	}
	
}
